package basicSyntax;

public class Variables {

	public static void main(String[] args) {
		int num1 = 61;
		double num2 = 6.01;
		System.out.println(num1);
		System.out.println(num2);
		
		String like = "私の好きな";
		String last = "です。";
		System.out.println(like + "整数は" + num1 + last);
		System.out.println(like + "小数は" + num2 + last);
	}

}
